    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $this->validate($request, [
            'product_Series' => 'required',
            'product_Model'  => 'required|unique:catalogues, product_Model'
        ]);
        $catalogue = new Catalogue();
        $catalogue->supplier_Company = $request['supplier_Company'];
        $catalogue->product_Family   = $request['product_Family'];
        $catalogue->product_Series   = $request['product_Series'];
        $catalogue->product_Model    = $request['product_Model'];
    }

